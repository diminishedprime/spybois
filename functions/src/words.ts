export const makeBoard = (): any[] => {
  // Shuffles in place
  shuffle(words);
  const first25 = words.slice(0, 25);
  const asCards = first25.map((word: string, idx: number) => {
    const id = uuid();
    const value = word;
    const flipped = false;
    let team = "";
    const numRed = 8;
    const numBlue = 8;
    const doubleAgent = 1;
    const assassin = 1;
    // const bystanders = 8;
    if (idx < numRed) {
      team = "team1";
    } else if (idx < numRed + numBlue) {
      team = "team2";
    } else if (idx < numRed + numBlue + doubleAgent) {
      team = Math.random() > 0.5 ? "team1" : "team2";
    } else if (idx < numRed + numBlue + doubleAgent + assassin) {
      team = "assassin";
    } else {
      team = "bystander";
    }
    return {
      id,
      value,
      flipped,
      team,
    };
  });
  shuffle(asCards);
  return asCards;
};
